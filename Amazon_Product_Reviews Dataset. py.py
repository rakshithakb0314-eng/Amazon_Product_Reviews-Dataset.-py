# ============================================================
# SENTIMENT ANALYSIS USING DISTILBERT
# ============================================================

import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
import re

from transformers import pipeline
from sklearn.metrics import classification_report, confusion_matrix

# ============================================================
# SAMPLE DATASET
# ============================================================

data = {
    "text": [
        "This product is amazing!",
        "I hate this item.",
        "Very good quality and fast delivery.",
        "Worst purchase ever.",
        "Excellent customer service.",
        "Not worth the money."
    ],
    "label": [
        "POSITIVE",
        "NEGATIVE",
        "POSITIVE",
        "NEGATIVE",
        "POSITIVE",
        "NEGATIVE"
    ]
}

df = pd.DataFrame(data)

# ============================================================
# TEXT CLEANING
# ============================================================

def clean_text(text):
    text = str(text).lower()
    text = re.sub(r'http\\S+|www\\S+', '', text)   # Remove URLs
    text = re.sub(r'[^a-zA-Z\\s]', '', text)       # Remove special chars
    text = text.strip()
    return text

df["clean_text"] = df["text"].apply(clean_text)

# ============================================================
# LOAD DISTILBERT SENTIMENT MODEL
# ============================================================

classifier = pipeline(
    "sentiment-analysis",
    model="distilbert-base-uncased-finetuned-sst-2-english"
)

# ============================================================
# PREDICTIONS
# ============================================================

predictions = classifier(df["clean_text"].tolist())

df["predicted"] = [pred["label"] for pred in predictions]

# ============================================================
# RESULTS
# ============================================================

print("\nPredictions:")
print(df[["text", "label", "predicted"]])

# ============================================================
# EVALUATION
# ============================================================

print("\nClassification Report:")
print(classification_report(df["label"], df["predicted"]))

cm = confusion_matrix(df["label"], df["predicted"])

# ============================================================
# VISUALIZATION
# ============================================================

plt.figure(figsize=(6, 4))
sns.heatmap(
    cm,
    annot=True,
    fmt="d",
    cmap="Blues",
    xticklabels=["NEGATIVE", "POSITIVE"],
    yticklabels=["NEGATIVE", "POSITIVE"]
)
plt.title("Confusion Matrix")
plt.xlabel("Predicted")
plt.ylabel("Actual")
plt.show()